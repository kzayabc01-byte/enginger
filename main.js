// Carousel arrow navigation
function initCarouselControls() {
  const carousel = document.getElementById('caseCarousel');
  const prevBtn = document.getElementById('carouselPrev');
  const nextBtn = document.getElementById('carouselNext');

  function getScrollAmount() {
    const card = carousel.querySelector('.snap-start');
    if (!card) return 400;
    return card.offsetWidth + 24; // width + gap
  }

  function updateArrowVisibility() {
    const tolerance = 10;
    if (carousel.scrollLeft <= tolerance) {
      prevBtn.style.opacity = '0.4';
      prevBtn.style.pointerEvents = 'none';
    } else {
      prevBtn.style.opacity = '0.85';
      prevBtn.style.pointerEvents = 'auto';
    }
    const maxScroll = carousel.scrollWidth - carousel.clientWidth;
    if (carousel.scrollLeft >= maxScroll - tolerance) {
      nextBtn.style.opacity = '0.4';
      nextBtn.style.pointerEvents = 'none';
    } else {
      nextBtn.style.opacity = '0.85';
      nextBtn.style.pointerEvents = 'auto';
    }
  }

  prevBtn.addEventListener('click', () => {
    carousel.scrollBy({ left: -getScrollAmount(), behavior: 'smooth' });
  });
  nextBtn.addEventListener('click', () => {
    carousel.scrollBy({ left: getScrollAmount(), behavior: 'smooth' });
  });

  // --- Carousel dots & counter ---
  const dotsContainer = document.getElementById('carouselDots');
  const counterEl = document.getElementById('carouselCounter');
  let activeDotIdx = 0;

  // Render dots
  if (dotsContainer) {
    dotsContainer.innerHTML = caseStudies.map((_, i) =>
      `<span class="carousel-dot ${i === 0 ? 'active' : ''}" data-idx="${i}"></span>`
    ).join('');

    // Click dot → scroll to card
    dotsContainer.querySelectorAll('.carousel-dot').forEach(dot => {
      dot.addEventListener('click', () => {
        const idx = parseInt(dot.dataset.idx);
        const card = carousel.querySelectorAll('.snap-start')[idx];
        if (card) {
          carousel.scrollTo({ left: card.offsetLeft - 24, behavior: 'smooth' });
        }
      });
    });
  }

  function updateIndicator() {
    const cards = carousel.querySelectorAll('.snap-start');
    if (!cards.length) return;
    const carouselCenter = carousel.scrollLeft + carousel.clientWidth / 2;
    let closestIdx = 0;
    let closestDist = Infinity;
    cards.forEach((card, i) => {
      const cardCenter = card.offsetLeft + card.offsetWidth / 2;
      const dist = Math.abs(carouselCenter - cardCenter);
      if (dist < closestDist) { closestDist = dist; closestIdx = i; }
    });
    if (closestIdx !== activeDotIdx) {
      activeDotIdx = closestIdx;
      if (dotsContainer) {
        dotsContainer.querySelectorAll('.carousel-dot').forEach((d, i) => {
          d.classList.toggle('active', i === activeDotIdx);
        });
      }
      if (counterEl) {
        counterEl.textContent = `${activeDotIdx + 1}/${caseStudies.length}`;
      }
    }
  }

  carousel.addEventListener('scroll', updateArrowVisibility, { passive: true });
  carousel.addEventListener('scroll', updateIndicator, { passive: true });
  window.addEventListener('resize', updateArrowVisibility);

  // Initial state
  setTimeout(updateArrowVisibility, 100);
  setTimeout(updateIndicator, 150);
  setTimeout(updateArrowVisibility, 500);

  // Drag to scroll
  let isDown = false, startX, scrollStart;
  carousel.addEventListener('mousedown', (e) => {
    isDown = true;
    carousel.classList.add('dragging');
    startX = e.pageX - carousel.offsetLeft;
    scrollStart = carousel.scrollLeft;
  });
  carousel.addEventListener('mouseleave', () => {
    isDown = false;
    carousel.classList.remove('dragging');
  });
  carousel.addEventListener('mouseup', () => {
    isDown = false;
    carousel.classList.remove('dragging');
  });
  carousel.addEventListener('mousemove', (e) => {
    if (!isDown) return;
    e.preventDefault();
    const x = e.pageX - carousel.offsetLeft;
    const walk = (x - startX) * 1.5;
    carousel.scrollLeft = scrollStart - walk;
  });
}

