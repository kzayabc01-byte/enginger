// 18. CURSOR SPARKLE TRAIL — canvas particle system
// ==============================================
function initCursorSparkles() {
  const canvas = document.getElementById('sparkleCanvas');
  if (!canvas) return;
  const ctx = canvas.getContext('2d');

  let W, H;
  function resize() { W = canvas.width = window.innerWidth; H = canvas.height = window.innerHeight; }
  resize();
  window.addEventListener('resize', resize);

  const particles = [];
  const MAX = 60;
  let mouseX = -100, mouseY = -100;
  let prevX = -100, prevY = -100;
  let isTouch = false;

  const colors = ['#10B981','#34D399','#6EE7B7','#F59E0B','#FBBF24','#EC4899','#8B5CF6','#3B82F6'];

  document.addEventListener('mousemove', (e) => {
    prevX = mouseX; prevY = mouseY;
    mouseX = e.clientX; mouseY = e.clientY;
  });
  document.addEventListener('touchstart', () => { isTouch = true; }, { once: true });

  function spawn() {
    if (isTouch) return;
    const dx = mouseX - prevX;
    const dy = mouseY - prevY;
    const speed = Math.hypot(dx, dy);
    const count = Math.min(Math.floor(speed / 4), 4);

    for (let i = 0; i < count; i++) {
      particles.push({
        x: mouseX + (Math.random() - 0.5) * 14,
        y: mouseY + (Math.random() - 0.5) * 14,
        vx: (Math.random() - 0.5) * 1.5 + dx * 0.02,
        vy: (Math.random() - 0.5) * 1.5 + dy * 0.02 + Math.random() * 0.5,
        life: 1,
        decay: 0.008 + Math.random() * 0.025,
        size: 1.5 + Math.random() * 3.5,
        color: colors[Math.floor(Math.random() * colors.length)]
      });
    }

    // Cap particles
    while (particles.length > MAX) particles.shift();
  }

  function draw() {
    ctx.clearRect(0, 0, W, H);

    for (let i = particles.length - 1; i >= 0; i--) {
      const p = particles[i];
      p.x += p.vx;
      p.y += p.vy;
      p.life -= p.decay;

      if (p.life <= 0) { particles.splice(i, 1); continue; }

      ctx.save();
      ctx.globalAlpha = p.life * 0.7;
      ctx.fillStyle = p.color;
      ctx.shadowColor = p.color;
      ctx.shadowBlur = 6;
      ctx.beginPath();
      ctx.arc(p.x, p.y, p.size * p.life, 0, Math.PI * 2);
      ctx.fill();
      ctx.restore();
    }

    requestAnimationFrame(draw);
  }

  // Spawn loop
  setInterval(spawn, 25);
  requestAnimationFrame(draw);
}

