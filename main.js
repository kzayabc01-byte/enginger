// 29. PROXIMITY GLOW — cards glow as cursor approaches
// ==============================================
function initProximityGlow() {
  const cards = document.querySelectorAll('.proximity-glow');
  if (!cards.length) return;

  let mouseX = -500, mouseY = -500;
  document.addEventListener('mousemove', (e) => { mouseX = e.clientX; mouseY = e.clientY; });

  function update() {
    cards.forEach(card => {
      const rect = card.getBoundingClientRect();
      const cx = rect.left + rect.width / 2;
      const cy = rect.top + rect.height / 2;
      const dist = Math.hypot(mouseX - cx, mouseY - cy);
      const maxDist = 400;

      if (dist < maxDist) {
        const intensity = 1 - dist / maxDist;
        card.style.boxShadow = `0 0 ${30 * intensity}px ${8 * intensity}px rgba(16,185,129,${0.2 * intensity}), 0 ${8 * intensity}px ${24 * intensity}px -${4 * intensity}px rgba(0,0,0,${0.08 * intensity})`;
        card.style.borderColor = `rgba(16,185,129,${0.15 + 0.25 * intensity})`;
      } else {
        card.style.boxShadow = '';
        card.style.borderColor = '';
      }
    });
    requestAnimationFrame(update);
  }
  requestAnimationFrame(update);
}

