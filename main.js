// 19. 3D TILT EFFECT — smooth spring-physics version
// ==============================================
function initTiltEffect() {
  document.querySelectorAll('.tilt-card').forEach(card => {
    let targetX = 0, targetY = 0;   // target rotation
    let currentX = 0, currentY = 0; // current (smoothed) rotation
    let isHovering = false;
    let animId = null;

    const maxTilt = 5;       // degrees — subtle and elegant
    const stiffness = 0.08;  // lower = softer, more lag
    const perspective = 1200;

    card.addEventListener('mousemove', (e) => {
      const rect = card.getBoundingClientRect();
      const x = (e.clientX - rect.left) / rect.width - 0.5;   // -0.5 ... 0.5
      const y = (e.clientY - rect.top) / rect.height - 0.5;   // -0.5 ... 0.5
      targetX = y * maxTilt;   // vertical mouse → rotateX
      targetY = x * maxTilt;   // horizontal mouse → rotateY
      isHovering = true;

      if (!animId) {
        animId = requestAnimationFrame(animate);
      }
    });

    card.addEventListener('mouseleave', () => {
      targetX = 0;
      targetY = 0;
      isHovering = false;
    });

    function animate() {
      // Spring-physics lerp toward target
      currentX += (targetX - currentX) * stiffness;
      currentY += (targetY - currentY) * stiffness;

      const nearZero = Math.abs(currentX) < 0.01 && Math.abs(currentY) < 0.01;

      if (!isHovering && nearZero) {
        // Snap to zero and stop
        card.style.transform = '';
        card.style.transition = 'transform 0.5s cubic-bezier(0.16, 1, 0.3, 1)';
        animId = null;
        // Clear transition after it completes
        setTimeout(() => { card.style.transition = ''; }, 500);
        return;
      }

      card.style.transition = 'none';
      card.style.transform = `perspective(${perspective}px) rotateX(${currentX.toFixed(2)}deg) rotateY(${currentY.toFixed(2)}deg) scale3d(1.015,1.015,1.015)`;

      animId = requestAnimationFrame(animate);
    }
  });
}

