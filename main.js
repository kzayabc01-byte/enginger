// 0b. SOUND ENGINE — Web Audio API click sounds
// ==============================================
const Sound = (() => {
  let ctx = null;
  function getCtx() {
    if (!ctx) {
      try { ctx = new (window.AudioContext || window.webkitAudioContext)(); } catch(e) {}
    }
    // Resume if suspended (browsers require user gesture)
    if (ctx && ctx.state === 'suspended') ctx.resume();
    return ctx;
  }

  function play(freq, type, duration, vol = 0.08, glideFreq = 0) {
    const c = getCtx();
    if (!c) return;
    const t = c.currentTime;
    const osc = c.createOscillator();
    const gain = c.createGain();
    osc.type = type;
    osc.frequency.setValueAtTime(freq, t);
    if (glideFreq) osc.frequency.linearRampToValueAtTime(glideFreq, t + duration);
    gain.gain.setValueAtTime(vol, t);
    gain.gain.exponentialRampToValueAtTime(0.001, t + duration);
    osc.connect(gain);
    gain.connect(c.destination);
    osc.start(t);
    osc.stop(t + duration);
  }

  return {
    /** Generic button click — short crisp pop */
    click()  { play(800, 'sine', 0.08, 0.06, 400); },
    /** Softer tap for cards / tags */
    tap()    { play(600, 'sine', 0.06, 0.04, 300); },
    /** Success / positive action */
    success(){ play(1200, 'sine', 0.12, 0.12); play(1600, 'sine', 0.10, 0.10, 1200); },
    /** Error / warning */
    error()  { play(200, 'triangle', 0.15, 0.10, 100); },
    /** Toggle / switch */
    toggle() { play(500, 'sine', 0.06, 0.06, 700); },
    /** Notification / message */
    notify() { play(1000, 'sine', 0.08, 0.10); play(1400, 'sine', 0.06, 0.08, 1200); },
    /** Typewriter key tap — very subtle */
    keytap() { play(400 + Math.random() * 200, 'sine', 0.03, 0.02, 200); },
    /** Send / submit */
    send()   { play(900, 'triangle', 0.10, 0.10, 600); },
    /** Expand / open */
    expand() { play(700, 'sine', 0.07, 0.08, 1000); },
    /** Collapse / close */
    collapse(){ play(1000, 'sine', 0.07, 0.06, 600); },
  };
})();

