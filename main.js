// 2. COUNT-UP ANIMATION — numbers animate on scroll
// ==============================================
function initCountUp() {
  const countObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (!entry.isIntersecting) return;
      const el = entry.target;
      const target = parseInt(el.dataset.count);
      if (isNaN(target)) return;

      const duration = 1200;
      const start = performance.now();
      function tick(now) {
        const elapsed = now - start;
        const progress = Math.min(elapsed / duration, 1);
        // Ease-out curve
        const eased = 1 - Math.pow(1 - progress, 3);
        const current = Math.round(eased * target);
        el.textContent = current + (el.dataset.suffix || '');
        if (progress < 1) requestAnimationFrame(tick);
        else el.textContent = target + (el.dataset.suffix || '');
      }
      requestAnimationFrame(tick);
      countObserver.unobserve(el);
    });
  }, { threshold: 0.6 });

  document.querySelectorAll('[data-count]').forEach(el => countObserver.observe(el));
}

// ==============================================
// 3. PARALLAX SCROLL — orbs drift at different speeds
// ==============================================
function initParallax() {
  let ticking = false;
  window.addEventListener('scroll', () => {
    if (!ticking) {
      requestAnimationFrame(() => {
        const scrollY = window.scrollY;
        // Slow parallax on section orbs
        document.querySelectorAll('.section-orb').forEach((orb, i) => {
          const speed = 0.03 + (i % 3) * 0.02;
          orb.style.transform = `translateY(${scrollY * speed}px)`;
        });
        // Hero blob moves opposite direction slightly
        const blob = document.querySelector('.morph-blob-container');
        if (blob) blob.style.transform = `translate(-50%, calc(-50% + ${scrollY * 0.04}px))`;
        ticking = false;
      });
      ticking = true;
    }
  }, { passive: true });
}

// ==============================================
// 4. INTERSECTION OBSERVER — Fade-up
// ==============================================
let fadeObserver;

function initScrollReveal() {
  fadeObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        fadeObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.15, rootMargin: '0px 0px -40px 0px' });

  document.querySelectorAll('[data-fade]').forEach(el => fadeObserver.observe(el));
}

function reObserve() {
  document.querySelectorAll('[data-fade]:not(.visible)').forEach(el => {
    if (fadeObserver) fadeObserver.observe(el);
  });
}

